#include <iostream>

int main()
{
    int n;
    std::cin >> n;

    if (n == 1) std::cout << "January\n";
    else if (n == 2) std::cout << "February\n";
    else if (n == 3) std::cout << "March\n";
    else if (n == 4) std::cout << "April\n";
    else if (n == 5) std::cout << "May\n";
    else if (n == 6) std::cout << "June\n";
    else if (n == 7) std::cout << "July\n";
    else if (n == 8) std::cout << "August\n";
    else if (n == 9) std::cout << "September\n";
    else if (n == 10) std::cout << "October\n";
    else if (n == 11) std::cout << "November\n";
    else if (n == 12) std::cout << "December\n";
    return 0;
}